    private String getGender() {
        if (jRadioMan.isSelected()) {
            return "Laki-laki";
        } else if (jRadioWoman.isSelected()) {
            return "Perempuan";
        } else {
            return "";
        }
    }

public double calculateBMI(String gender, int age, double weight, double height) {

    // Hitung indeks massa tubuh
    double bmi = weight / (height * height);

    // Konversi indeks massa tubuh ke satuan kg/m^2
    bmi = bmi * 10000;

    // Tentukan kategori BMI
    String category;
    if (gender.equals("Laki-laki")) {
        if (bmi < 18.5) {
            category = "kurus";
        } else if (bmi < 24.9) {
            category = "normal";
        } else if (bmi < 29.9) {
            category = "kegemukan tingkat I";
        } else if (bmi < 34.9) {
            category = "kegemukan tingkat II";
        } else {
            category = "kegemukan tingkat III";
        }
    } else {
        if (bmi < 18.5) {
            category = "kurus";
        } else if (bmi < 22.9) {
            category = "kormal";
        } else if (bmi < 29.9) {
            category = "kegemukan tingkat I";
        } else if (bmi < 34.9) {
            category = "kegemukan tingkat II";
        } else {
            category = "kegemukan tingkat III";
        }
    }

    // Kembalikan nilai BMI dan kategorinya
    return bmi;
}

    
    private double calculateBMR(String gender, int age, double weight, double height) {
    double bmr = 0;

    if (gender.equals("Laki-laki")) {
        bmr = 66.5 + (13.7 * weight) + (5.0 * height) - (6.8 * age);
    } else if (gender.equals("Perempuan")) {
        bmr = 655 + (9.6 * weight) + (1.8 * height) - (4.7 * age);
    }

    return bmr;
}
    // Tambahkan variabel baru untuk menyimpan kisaran berat badan
    private double[] weightRange;

    // Tambahkan method baru untuk menghitung kisaran berat badan
    private double[] calculateWeightRange(double height) {
    // Definisikan nilai minimum dan maksimum kisaran berat badan
    double minWeight = 0;
    double maxWeight = 0;

    // Hitung berat badan minimum dan maksimum
    minWeight = 18.5 * ((height / 100) * (height / 100));
    maxWeight = 23.0 * ((height / 100) * (height / 100));

    // Buat array untuk menyimpan kisaran berat badan
    weightRange = new double[2];

    // Masukkan nilai minimum dan maksimum kisaran berat badan ke dalam array
    weightRange[0] = minWeight;
    weightRange[1] = maxWeight;
    // Kembalikan array kisaran berat badan
    return weightRange;
}
    
