    private void jButtonHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHitungActionPerformed
        // Ambil nilai dari radio button dan text field
        String gender = getGender();
        // Cek apakah radio button "Laki-laki" dipilih
        if (jRadioMan.isSelected()) {
            // Tetapkan radio button "Perempuan" tidak dipilih
            jRadioWoman.setSelected(false);
        } else if (jRadioWoman.isSelected()) {
            // Tetapkan radio button "Laki-laki" tidak dipilih
            jRadioMan.setSelected(false);
        }
        int age = Integer.parseInt(jTextFieldAge.getText());
        double height = Double.parseDouble(jTextFieldHeight.getText());
        double weight = Double.parseDouble(jTextFieldWeight.getText());

        // Hitung BMI dan BMR
        double bmi = calculateBMI(gender, age, weight, height);
        double bmr = calculateBMR(gender, age, weight, height);
        
        String category;
        if (gender.equals("Laki-laki")) {
            if (bmi < 18.5) {
                category = "kurus";
            } else if (bmi < 24.9) {
                category = "normal";
            } else if (bmi < 29.9) {
                category = "obesitas tingkat I";
            } else if (bmi < 34.9) {
                category = "obesitas tingkat II";
            } else {
                category = "obesitas tingkat III";
            }
        } else {
            if (bmi < 18.5) {
                category = "kurus";
            } else if (bmi < 22.9) {
                category = "normal";
            } else if (bmi < 29.9) {
                category = "obesitas tingkat I";
            } else if (bmi < 34.9) {
                category = "obesitas tingkat II";
            } else {
                category = "obesitas tingkat III";
            }
        }
        
        // Hitung kisaran berat badan
        weightRange = calculateWeightRange(height);        
        
        // Tampilkan hasil pada panel jPanel3
        jLabelBMI.setText("BMI Anda adalah " + String.format("%.2f", bmi));
        jLabelCategory.setText("Kategori berat badan Anda " + category);
        jLabelBMR.setText("BMR Anda adalah " + String.format("%.2f", bmr) + " kcal/hari");
        jLabelWeightRange.setText("Maka rekomendasi berat badan Anda adalah " 
            + String.format("%.2f", weightRange[0]) + " - " + String.format("%.2f", weightRange[1]) + " kg");


        jPanel3.revalidate();
        jPanel3.repaint();
    }//GEN-LAST:event_jButtonHitungActionPerformed

